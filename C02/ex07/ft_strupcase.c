char	*ft_strupcase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if ('a' <= c && c <= 'z')
			str[i] = c - 32;
		i++;
	}
	return (str);
}
