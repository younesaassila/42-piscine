char	*ft_strlowcase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if ('A' <= c && c <= 'Z')
			str[i] = c + 32;
		i++;
	}
	return (str);
}
